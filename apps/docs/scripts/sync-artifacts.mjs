import { existsSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import {
  normalizeOrigin,
  syncDocsFromArtifacts,
} from "@b4r7x/registry-kit";
import {
  assertArtifactSyncOutputs,
  getArtifactLibraries,
  readDocsLibrariesConfig,
  resolveArtifactSyncMode,
} from "./sync-artifacts-lib.mjs";

const DOCS_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const WORKSPACE_ROOT = resolve(DOCS_ROOT, "../../..");
const DOCS_LIBRARIES_CONFIG_PATH = resolve(DOCS_ROOT, "config/docs-libraries.json");

const DIFFGAZER_ORIGIN = "https://diffgazer.com";

const docsLibraries = readDocsLibrariesConfig(DOCS_LIBRARIES_CONFIG_PATH);
const artifactLibraries = getArtifactLibraries(docsLibraries);

syncDocsFromArtifacts({
  docsRoot: DOCS_ROOT,
  workspaceRoot: WORKSPACE_ROOT,
  libraries: artifactLibraries,
  primaryLibraryId: docsLibraries.primaryLibraryId,
  origin: normalizeOrigin(process.env.REGISTRY_ORIGIN, { defaultOrigin: DIFFGAZER_ORIGIN }),
  sourceOrigin: DIFFGAZER_ORIGIN,
  mode: resolveArtifactSyncMode(process.env, {
    libraries: artifactLibraries,
    resolveFromDir: DOCS_ROOT,
  }),
});

// Generate demo-loaders.ts from discovered demo-index files.
const demoEntries = artifactLibraries
  .filter((lib) => existsSync(join(DOCS_ROOT, "src/generated", lib.id, "demo-index.ts")))
  .map((lib) => `  "${lib.id}": () => import("./${lib.id}/demo-index"),`);

const demoLoadersContent = [
  "// Auto-generated by prepare:generated. Do not edit.",
  `import type { ComponentType, LazyExoticComponent } from "react"`,
  `export type DemoMap = Record<string, LazyExoticComponent<ComponentType>>`,
  `export const demoLoaders: Record<string, () => Promise<{ demos: DemoMap }>> = {`,
  ...demoEntries,
  `}`,
  "",
].join("\n");

writeFileSync(join(DOCS_ROOT, "src/generated/demo-loaders.ts"), demoLoadersContent);

// Generate library-data.ts from discovered data files per library.
const libraryDataImports = [];
const hooksDataEntries = [];
const libsDataEntries = [];

for (const lib of artifactLibraries) {
  const genDir = join(DOCS_ROOT, "src/generated", lib.id);
  if (!existsSync(genDir)) continue;

  const files = readdirSync(genDir).filter((f) => f.endsWith(".json"));

  for (const file of files) {
    const varName = `${lib.id.replace(/-/g, "_")}_${file.replace(/-/g, "_").replace(".json", "")}`;

    if (file.endsWith("-hooks.json")) {
      libraryDataImports.push(`import ${varName} from "./${lib.id}/${file}"`);
      hooksDataEntries.push(`  "${lib.id}": ${varName},`);
    } else if (file.endsWith("-libs.json")) {
      libraryDataImports.push(`import ${varName} from "./${lib.id}/${file}"`);
      libsDataEntries.push(`  "${lib.id}": ${varName},`);
    }
  }
}

const libraryDataContent = [
  "// Auto-generated by prepare:generated. Do not edit.",
  ...libraryDataImports,
  "",
  "// eslint-disable-next-line @typescript-eslint/no-explicit-any",
  `export const hooksData: Record<string, Record<string, any>> = {`,
  ...hooksDataEntries,
  `}`,
  "",
  "// eslint-disable-next-line @typescript-eslint/no-explicit-any",
  `export const libsData: Record<string, Record<string, any>> = {`,
  ...libsDataEntries,
  `}`,
  "",
].join("\n");

writeFileSync(join(DOCS_ROOT, "src/generated/library-data.ts"), libraryDataContent);

assertArtifactSyncOutputs({
  docsRoot: DOCS_ROOT,
  primaryLibraryId: docsLibraries.primaryLibraryId,
  libraries: artifactLibraries,
});
