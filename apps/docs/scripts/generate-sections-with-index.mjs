import { mkdir, readdir, stat, writeFile } from "node:fs/promises";
import { dirname, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_PATH = fileURLToPath(import.meta.url);
const DOCS_ROOT = resolve(dirname(SCRIPT_PATH), "..");
const CONTENT_DOCS = resolve(DOCS_ROOT, "content/docs");
const OUTPUT_PATH = resolve(DOCS_ROOT, "src/generated/sections-with-index.ts");

export async function collectSectionsWithIndex(dir, options = {}) {
  const contentDocsDir = options.contentDocsDir ?? dir;
  const found = options.found ?? new Set();

  for (const entry of await readdir(dir)) {
    const full = resolve(dir, entry);
    if ((await stat(full)).isDirectory()) {
      await collectSectionsWithIndex(full, { contentDocsDir, found });
    } else if (entry === "index.mdx") {
      found.add(relative(contentDocsDir, dir).split(/[/\\]/).join("/"));
    }
  }
  return found;
}

export function renderSectionsWithIndex(sections) {
  return `// Generated by apps/docs/scripts/generate-sections-with-index.mjs. Do not edit.\nexport const SECTIONS_WITH_INDEX = new Set<string>([\n${sections
    .map((section) => `\t${JSON.stringify(section)},`)
    .join("\n")}\n]);\n`;
}

export async function generateSectionsWithIndex(options = {}) {
  const contentDocsDir = options.contentDocsDir ?? CONTENT_DOCS;
  const outputPath = options.outputPath ?? OUTPUT_PATH;
  const log = options.log ?? console.log;
  const sections = [...(await collectSectionsWithIndex(contentDocsDir))].filter(Boolean).sort();
  const contents = renderSectionsWithIndex(sections);

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, contents, "utf8");

  log(`[sections-with-index] wrote ${sections.length} sections to ${outputPath}`);
  return { sections, outputPath };
}

if (process.argv[1] === SCRIPT_PATH) {
  await generateSectionsWithIndex();
}
