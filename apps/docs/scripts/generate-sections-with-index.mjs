import { mkdir, readdir, stat, writeFile } from "node:fs/promises";
import { dirname, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const DOCS_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const CONTENT_DOCS = resolve(DOCS_ROOT, "content/docs");
const OUTPUT_PATH = resolve(DOCS_ROOT, "src/generated/sections-with-index.ts");

/**
 * Section paths (relative to content/docs) that ship an `index.mdx`, e.g.
 * `content/docs/app/cli/index.mdx` -> `app/cli`. Breadcrumbs only links a section
 * whose path is in this set, so deriving it from disk keeps the single source of
 * truth in the filesystem instead of a hand-maintained constant.
 */
async function collectSectionsWithIndex(dir, found) {
  for (const entry of await readdir(dir)) {
    const full = resolve(dir, entry);
    if ((await stat(full)).isDirectory()) {
      await collectSectionsWithIndex(full, found);
    } else if (entry === "index.mdx") {
      found.add(relative(CONTENT_DOCS, dir).split(/[/\\]/).join("/"));
    }
  }
  return found;
}

const sections = [...(await collectSectionsWithIndex(CONTENT_DOCS, new Set()))]
  .filter(Boolean)
  .sort();

const contents = `// Generated by apps/docs/scripts/generate-sections-with-index.mjs. Do not edit.\nexport const SECTIONS_WITH_INDEX = new Set<string>([\n${sections
  .map((section) => `\t${JSON.stringify(section)},`)
  .join("\n")}\n]);\n`;

await mkdir(dirname(OUTPUT_PATH), { recursive: true });
await writeFile(OUTPUT_PATH, contents, "utf8");

console.log(`[sections-with-index] wrote ${sections.length} sections to ${OUTPUT_PATH}`);
