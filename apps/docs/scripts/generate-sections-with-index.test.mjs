import {
  existsSync,
  mkdirSync,
  mkdtempSync,
  readdirSync,
  readFileSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { afterEach, describe, expect, it } from "vitest";
import { SECTIONS_WITH_INDEX } from "@/generated/sections-with-index";
import { generateSectionsWithIndex } from "./generate-sections-with-index.mjs";

const DOCS_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const CONTENT_DOCS = resolve(DOCS_ROOT, "content/docs");

const tempRoots = [];

afterEach(() => {
  while (tempRoots.length > 0) {
    rmSync(tempRoots.pop(), { recursive: true, force: true });
  }
});

function makeTempRoot() {
  const root = mkdtempSync(join(tmpdir(), "dg-docs-sections-"));
  tempRoots.push(root);
  return root;
}

function writeText(root, relPath, content) {
  const path = join(root, relPath);
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, content);
}

/**
 * Section paths (relative to content/docs) that ship an `index.mdx`, e.g.
 * `content/docs/app/cli/index.mdx` -> `app/cli`. Breadcrumbs only links a section
 * when its path is in this set, so the generated set must match the disk tree.
 */
function sectionsWithIndexOnDisk() {
  const found = new Set();

  const walk = (dir) => {
    for (const entry of readdirSync(dir)) {
      const full = resolve(dir, entry);
      if (statSync(full).isDirectory()) {
        walk(full);
      } else if (entry === "index.mdx") {
        found.add(relative(CONTENT_DOCS, dir).split("/").join("/"));
      }
    }
  };

  walk(CONTENT_DOCS);
  return found;
}

describe("generateSectionsWithIndex", () => {
  it("writes section paths only for directories that contain index.mdx", async () => {
    const root = makeTempRoot();
    const contentDocsDir = join(root, "content/docs");
    const outputPath = join(root, "src/generated/sections-with-index.ts");

    writeText(root, "content/docs/app/index.mdx", "# App\n");
    writeText(root, "content/docs/app/cli/index.mdx", "# CLI\n");
    writeText(root, "content/docs/app/cli/add.mdx", "# Add\n");
    writeText(root, "content/docs/app/concepts/overview.mdx", "# Overview\n");

    const result = await generateSectionsWithIndex({
      contentDocsDir,
      outputPath,
      log: () => {},
    });

    expect(result.sections).toEqual(["app", "app/cli"]);
    expect(readFileSync(outputPath, "utf8")).toBe(
      [
        "// Generated by apps/docs/scripts/generate-sections-with-index.mjs. Do not edit.",
        "export const SECTIONS_WITH_INDEX = new Set<string>([",
        '\t"app",',
        '\t"app/cli",',
        "]);",
        "",
      ].join("\n"),
    );
  });
});

describe("SECTIONS_WITH_INDEX", () => {
  it("matches the index.mdx pages present in content/docs", () => {
    // Generated by `prepare:generated` (run by pretest). Assert the directory
    // exists so the set comparison below can never pass vacuously on a clean
    // checkout, and that the generated set tracks the real content tree.
    expect(existsSync(CONTENT_DOCS)).toBe(true);

    expect([...SECTIONS_WITH_INDEX].sort()).toEqual([...sectionsWithIndexOnDisk()].sort());
  });
});
