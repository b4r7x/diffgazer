import { mkdirSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join } from "node:path";
import { afterEach, describe, expect, it } from "vitest";
import { generateSectionsWithIndex } from "./generate-sections-with-index.mjs";

const tempRoots = [];

afterEach(() => {
  while (tempRoots.length > 0) {
    rmSync(tempRoots.pop(), { recursive: true, force: true });
  }
});

function makeTempRoot() {
  const root = mkdtempSync(join(tmpdir(), "dg-docs-sections-"));
  tempRoots.push(root);
  return root;
}

function writeText(root, relPath, content) {
  const path = join(root, relPath);
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, content);
}

describe("generateSectionsWithIndex", () => {
  it("writes section paths only for directories that contain index.mdx", async () => {
    const root = makeTempRoot();
    const contentDocsDir = join(root, "content/docs");
    const outputPath = join(root, "src/generated/sections-with-index.ts");

    writeText(root, "content/docs/app/index.mdx", "# App\n");
    writeText(root, "content/docs/app/cli/index.mdx", "# CLI\n");
    writeText(root, "content/docs/app/cli/add.mdx", "# Add\n");
    writeText(root, "content/docs/app/concepts/overview.mdx", "# Overview\n");

    const result = await generateSectionsWithIndex({
      contentDocsDir,
      outputPath,
      log: () => {},
    });

    expect(result.sections).toEqual(["app", "app/cli"]);
    expect(readFileSync(outputPath, "utf8")).toBe(
      [
        "// Generated by apps/docs/scripts/generate-sections-with-index.mjs. Do not edit.",
        "export const SECTIONS_WITH_INDEX = new Set<string>([",
        '\t"app",',
        '\t"app/cli",',
        "]);",
        "",
      ].join("\n"),
    );
  });
});
