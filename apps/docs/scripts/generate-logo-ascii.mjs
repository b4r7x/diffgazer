import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import figlet from "figlet";

const DOCS_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const CONFIG_PATH = resolve(DOCS_ROOT, "config/docs-libraries.json");
const OUTPUT_PATH = resolve(DOCS_ROOT, "src/generated/logo-ascii.ts");
const FONT = "Small";

const { libraries } = JSON.parse(await readFile(CONFIG_PATH, "utf8"));

const entries = libraries
	.filter((lib) => typeof lib.logoText === "string" && lib.logoText.length > 0)
	.map((lib) => [lib.id, figlet.textSync(lib.logoText, { font: FONT })]);

// Section headings rendered as ASCII art in the same figlet font as the logo, so
// the home page heading reads as banner type while a sibling sr-only <h1> owns the
// accessible name.
const headings = [["documentation", "Documentation"]].map(([key, text]) => [
	key,
	figlet.textSync(text, { font: FONT }),
]);

function toRecordBody(pairs) {
	return pairs
		.map(([id, ascii]) => `  ${JSON.stringify(id)}: ${JSON.stringify(ascii)},`)
		.join("\n");
}

const contents = `// Generated by apps/docs/scripts/generate-logo-ascii.mjs. Do not edit.\nexport const LOGO_ASCII: Record<string, string> = {\n${toRecordBody(entries)}\n};\n\nexport const HEADING_ASCII: Record<string, string> = {\n${toRecordBody(headings)}\n};\n`;

await mkdir(dirname(OUTPUT_PATH), { recursive: true });
await writeFile(OUTPUT_PATH, contents, "utf8");

console.log(
	`[logo-ascii] wrote ${entries.length} logo + ${headings.length} heading entries to ${OUTPUT_PATH}`,
);
